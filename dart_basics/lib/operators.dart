void main() {
  int a = 10;
  int b = 5;

  print('Addition: ${a + b}');
  print('Subtraction: ${a - b}');
  print('Multiplication: ${a * b}');
  print('Division: ${a / b}');

  // Relational operators
  print('Is a greater than b? ${a > b}');

  // Logical operators
  bool c = true;
  bool d = false;
  print('Logical AND: ${c && d}');
}
