void main() {
  // For loop
  for (int i = 1; i <= 5; i++) {
    print('For Loop iteration $i');
  }

  // While loop
  int j = 1;
  while (j <= 3) {
    print('While Loop iteration $j');
    j++;
  }

  // Do-While loop
  int k = 1;
  do {
    print('Do-While Loop iteration $k');
    k++;
  } while (k <= 2);
}
